# 👉  Comparison Operators
# 🎏 Operator:- == (Equal), != (Not Equal), > (Greater than), < (Less than), >= (Greater than or equal to), <= (Less than or equal to).

# 🌠🌠🌠🌠🌠🌠🌠🌠🌠🌠🌠🌠🌠🌠🌠🌠🌠🌠🌠🌠🌠🌠🌠🌠🌠🌠🌠🌠🌠🌠🌠🌠🌠
x = 10;
y = 20;

print(x == y); # 🎉 Output:- False 
print(x != y); # 🎉 Output:- True
print(x < y); # 🎉 Output:- True
print(x > y); # 🎉 Output:- False
print(x <= y); # 🎉 Output:- True
print(x >= y); # 🎉 Output:- False