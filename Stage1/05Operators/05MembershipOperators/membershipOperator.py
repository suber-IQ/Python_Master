# 👉  Membership Operators
# 🎏 Operator:- (in) , (not in)

# 🌠🌠🌠🌠🌠🌠🌠🌠🌠🌠🌠🌠🌠🌠🌠🌠🌠🌠🌠🌠🌠🌠🌠🌠🌠🌠🌠🌠🌠🌠🌠🌠🌠

string1 = "hello"
print('H' in string1)  # 🎉 Output:- False
print('h' in string1)  # 🎉 Output:- True

l = [10,20,30,40,50,60,70,80] # List
print(58 in l) # 🎉 Output:- False 
print(50 in l) # 🎉 Output:- True 
print(50 not in l) # 🎉 Output:- False
