# 👉  Bitwise Operators
# 🎏 Operator:- & (AND), | (OR), ^ (XOR)

# 🌠🌠🌠🌠🌠🌠🌠🌠🌠🌠🌠🌠🌠🌠🌠🌠🌠🌠🌠🌠🌠🌠🌠🌠🌠🌠🌠🌠🌠🌠🌠🌠🌠

#  64, 32, 16, 8, 4, 2, 1 

x = 28; 
y = 14;
print(bin(x)) # 🎉 Output:- 11100
print(bin(y)) # 🎉 Output:- 01110

print(x & y,bin(x & y))  # 🎉 Output:- 12 (1100)
print(x | y,bin(x | y))  # 🎉 Output:- 30 (11110)
print(x ^ y,bin(x ^ y))  # 🎉 Output:- 18 (10010)


